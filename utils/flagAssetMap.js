export function getFlagAssetFromPath(flagPath) {
  if (!flagPath) {
    return null;
  }

  const normalized = String(flagPath)
    .trim()
    .replace(/\\/g, '/')
    .replace(/^[./]+/, '')
    .replace(/^\/+/, '');

  const withoutAssetsPrefix = normalized.replace(/^assets\//, '');
  const fileName = withoutAssetsPrefix.split('/').pop();
  const candidates = [
    normalized,
    'assets/' + withoutAssetsPrefix,
  ];

  if (fileName) {
    candidates.push('assets/flags_new/' + fileName);
    candidates.push('assets/flags/' + fileName);
  }

  for (const key of candidates) {
    if (FLAG_ASSET_MAP[key]) {
      return FLAG_ASSET_MAP[key];
    }
  }

  return null;
}
